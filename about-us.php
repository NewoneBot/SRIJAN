                    <p>
                    Born on January 26, 1979, in the picturesque village of Padma, nestled in the district of Madhubani, art has been an integral part of my life since childhood. I inherited my love for Madhubani painting from my grandmother, who passed down the intricate techniques and rich traditions of this ancient art form.
                    </p>
                    <p>Despite growing up in a rural setting, my thirst for knowledge and creativity knew no bounds. I completed my 10th-grade education in my village, but my journey took a significant turn when I married Rajesh Kumar Choudhary in 1996 and moved to Darbhanga. It was here that my husband became my guiding light, encouraging me to pursue further education and explore my passion for art.</p>
                    <p>Even as life presented new challenges and responsibilities, I continued to nurture my love for Madhubani painting, creating intricate masterpieces in my free time. Recognizing my talent and dedication, my husband played a pivotal role in transforming my hobby into a career.</p>
                    <p>In 2002, fueled by a desire to share the beauty of Madhubani painting with the world, I founded Srijan Mithila. What began as a humble initiative has blossomed into a renowned institution, empowering thousands of aspiring artists to unleash their creativity and embrace the rich cultural heritage of Mithila.</p>
                    <h5>Founder and Mentor</h5>
                    <p>As the founder and mentor of Srijan Mithila, I am committed to nurturing talent, preserving tradition, and inspiring the next generation of artists. My journey from a small village to the helm of an esteemed art institution is a testament to the transformative power of passion, perseverance, and the timeless art of Madhubani painting.</p>
