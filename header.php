    <script>
    $(document).ready(function() {
        $('.loginform').submit(function(event) {
            event.preventDefault(); // Prevent the default form submission

            // Capture form data
            var formData = {
                email: $('input[name=email]').val(),
                password: $('input[name=password]').val()
            };
            console.log("hello", formData);

            // Send AJAX request
            $.ajax({
                url: 'logindb.php',
                type: 'POST',
                data: formData,
                dataType: 'json',
                success: function(response) {
                    if (response.success) {
                        console.log("data", response.sessionData.payment);
                        localStorage.setItem('sessionData', response.sessionData);

                        // Retrieve sessionData from localStorage
                        var storedSessionData = JSON.parse(localStorage.getItem(
                            'sessionData'));
                        if (storedSessionData.payment == 0) {
                            if (storedSessionData.login_type == 1 || storedSessionData
                                .login_type == 0) {

                                window.location.href = 'admin/index.php';
                            } else {
                                window.location.href = 'admin/payment.php';
                            }
                        } else if (storedSessionData.payment == 1) {
                            window.location.href = 'admin/index.php';
                        }
                    } else {
                        $('#loginMessage').text(response.message);
                        console.log(response);
                    }
                },
                error: function(xhr, status, error) {
                    console.error("Error:", error);
                }
            });
        });
    });
    </script>